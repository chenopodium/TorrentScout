    private void checkColors(JButton btn) {
        Color c = btn.getBackground();
        int n = c.getRed() + c.getBlue()+ c.getGreen();
        if (n/3<75) {
            btn.setForeground(Color.white);
        }
        else btn.setForeground(Color.black);
    }
