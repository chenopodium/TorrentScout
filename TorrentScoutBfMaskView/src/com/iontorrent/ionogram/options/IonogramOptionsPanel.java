    void load() {
        // TODO read settings and initialize GUI
        // Example:        
        // someCheckBox.setSelected(Preferences.userNodeForPackage(IonogramOptionsPanel.class).getBoolean("someFlag", false));
        // or for org.openide.util with API spec. version >= 7.4:
        // someCheckBox.setSelected(NbPreferences.forModule(IonogramOptionsPanel.class).getBoolean("someFlag", false));
        // or:
        // someTextField.setText(SomeSystemOption.getDefault().getSomeStringProperty());
        Preferences p =  Preferences.userNodeForPackage(IonogramOptionsPanel.class);
        String chart_type = p.get("chart_type", "standard bar chart");
        this.jComboBox1.setSelectedItem(chart_type);
        String colT = p.get("color_T", "AA0000");
        String colA = p.get("color_A", "00AA00");
        String colC = p.get("color_C", "0000AA");
        String colG = p.get("color_G", "000000");
        btnG.setBackground(GuiUtils.stringToColor(colG));
        btnA.setBackground(GuiUtils.stringToColor(colA));
        btnT.setBackground(GuiUtils.stringToColor(colT));
        btnC.setBackground(GuiUtils.stringToColor(colC));
        checkColors(btnG);
        checkColors(btnA);
        checkColors(btnT);
        checkColors(btnC);
    }

   

